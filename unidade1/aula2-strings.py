#String
print('Olá')
#tipo String
print(type('Ola'))
#Aspas duplas para poder utilizar aspas simples dentro
print("Olá'''")
print('Olá"""')
#len apresenta o tamanho
print(len("tamanho"))

s = 'Valor dentro de uma variavel'
print(s)
#retorna o valor na posicao
print(s[3])
#retorna os valores da string entre as posições
print(s[3:])
print(s[:3])
print(s[2:3])
#com o valor negativo, começa de tras
print(s[-4:-2])
print(s[-1:])
print(s[:-4])

#intervalo entre os valores, por padrão é 1
print(s[::2])
print(s[::3])
#pega de trás para frente
print(s[::-1])
print(s[::-3])


